'use strict';


/**
 * Get All The Paintings
 *
 * returns String
 **/
exports.getApiV1Paintings = function() {
  return new Promise(function(resolve, reject) {
    var examples = {};
    examples['application/json'] = "";
    if (Object.keys(examples).length > 0) {
      resolve(examples[Object.keys(examples)[0]]);
    } else {
      resolve();
    }
  });
}


